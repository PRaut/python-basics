#!/bin/python3

#Math

print(50 + 50) #add

print(50 - 50) #subs

print(50 * 50) #mul

print(50 / 50) #divide

print(50 + 50 * 50 - 50 / 50) #PEMDAS

print(50 % 2) #modulo

print(50 ** 2) #exponents

print(50 / 6) # returns quotient with decimal output i.e 8.333333333333334

# To get no leftovers in quotient use //

print(50 // 6) # No leftover returns 8